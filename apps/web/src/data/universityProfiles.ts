// Lookup helpers
export function getProfileBySlug(slug: string): UniversityProfile | undefined {
  return universityProfiles.find((p) => p.slug === slug);
}

/** Returns the logo path for a university by its profile slug, or undefined. */
export function getUniversityLogo(profileSlug: string): string | undefined {
  return LOGO[profileSlug];
}

export function getProfilesByCountry(countrySlug: string): UniversityProfile[] {
  return universityProfiles.filter((p) => p.countrySlug === countrySlug);
}

